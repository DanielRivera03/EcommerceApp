using System;
using System.Collections.Generic;
using System.Linq;
using System.ServiceModel.Configuration;
using System.Web;
using System.Web.Mvc;

namespace ArquitectosAlmacenes.Filter
{
    // VALIDAR SI USUARIO HA INICIADO SESION
    public class ValidarSessionAttribute : ActionFilterAttribute
    {
        public override void OnActionExecuted(ActionExecutedContext filterContext)
        {
            if (HttpContext.Current.Session["UsuarioUnico"] == null)
            {
                filterContext.Result = new RedirectResult("~/Home/SesionExpiradaUsuarios");
                return;
            }
           
            base.OnActionExecuted(filterContext);
        }
    }
}